package main

import (
	"fmt"
	"net"
	"os"
)

/*
Accept an IP address from the command and do the equivalent of a netcat with a string
We're going to base64 encode this program and encode it into the other one. When the other program
starts it's going to put this into the writable tmpfs, if the exploit is written properly they will overwrite
this program with their own and get access!
*/
func main() {
	//	fmt.Println("generatereport started")
	connStr := fmt.Sprintf("%s:2223", os.Args[1])
	conn, err := net.Dial("tcp", connStr)
	// send to socket
	if err == nil {
		fmt.Fprintf(conn, "## generated by generatereport\n## generatereport failed. Error: unknown")
	}
}
